#include <stdio.h>

int main() {
    // Khai bao va gan gia tri cho bien canh hinh vuong
    int canh = 4;

    // Tinh chu vi: chu_vi = 4 * canh
    int chu_vi = 4 * canh;

    // Tinh dien tich: dien_tich = canh * canh
    int dien_tich = canh * canh;

    // In ket qua ra man hinh
    printf("Canh hinh vuong: %d\n", canh);
    printf("Chu vi hinh vuong: %d\n", chu_vi);
    printf("Dien tich hinh vuong: %d\n", dien_tich);

    return 0;
}

